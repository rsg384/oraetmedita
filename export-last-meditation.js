// Fun√ß√£o para obter a √∫ltima medita√ß√£o criada
function getLastCreatedMeditation() {
    console.log('üîç Buscando √∫ltima medita√ß√£o criada...');
    
    try {
        // Buscar medita√ß√µes do localStorage
        const meditations = JSON.parse(localStorage.getItem('meditations') || '[]');
        
        if (meditations.length === 0) {
            console.log('‚ùå Nenhuma medita√ß√£o encontrada no localStorage');
            return null;
        }
        
        // Ordenar por data de cria√ß√£o (mais recente primeiro)
        const sortedMeditations = meditations.sort((a, b) => {
            const dateA = new Date(a.createdAt || a.created_at || 0);
            const dateB = new Date(b.createdAt || b.created_at || 0);
            return dateB - dateA;
        });
        
        const lastMeditation = sortedMeditations[0];
        console.log('‚úÖ √öltima medita√ß√£o encontrada:', lastMeditation);
        
        return lastMeditation;
        
    } catch (error) {
        console.error('‚ùå Erro ao buscar √∫ltima medita√ß√£o:', error);
        return null;
    }
}

