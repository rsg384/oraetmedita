// Fun√ß√£o para exportar dados como arquivo JSON
function exportMeditationData(data, filename = null) {
    console.log('üì§ Exportando dados da medita√ß√£o...');
    
    try {
        // Gerar nome do arquivo se n√£o fornecido
        if (!filename) {
            const timestamp = new Date().toISOString().split('T')[0].replace(/-/g, '-');
            const meditationTitle = data.meditation.title.replace(/[^a-zA-Z0-9]/g, '_').substring(0, 20);
            filename = `meditation_${meditationTitle}_${timestamp}.json`;
        }
        
        // Converter para JSON
        const jsonString = JSON.stringify(data, null, 2);
        
        // Criar blob e download
        const blob = new Blob([jsonString], { type: 'application/json' });
        const url = URL.createObjectURL(blob);
        
        const a = document.createElement('a');
        a.href = url;
        a.download = filename;
        document.body.appendChild(a);
        a.click();
        document.body.removeChild(a);
        URL.revokeObjectURL(url);
        
        console.log('‚úÖ Arquivo exportado:', filename);
        return filename;
        
    } catch (error) {
        console.error('‚ùå Erro ao exportar dados:', error);
        return null;
    }
}

